package sheet3ControlFlow;
public class Ex1WhileDoForLoops{
    public static void main(String [] args){
	int i=0;
	while(i<=22){
	System.out.print("i is " + i + ",");
	i++;
	}
	int b=0;
	do{System.out.println("\n b is " + b);
	b++;
	}
	while(b<=22);
	for(int d=0; d<23; d++)
	{System.out.println("\n d is " + d);}
	}
	}