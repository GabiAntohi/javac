package sheet3ControlFlow;
public class Ex2Loops{
    public static void main(String [] args){
	int i=0;
	int c=100;
	while(i<50){
	c = c+2;
	System.out.println("c is " + c);
	i++;
	}
	int d=98;
	while(d<200){
	d = d+2;
	System.out.println("d is " + d);
	}
	for(int e=98;e<200;)
	{
	e=e+2;
	System.out.println("\n e is " + e);
	}
	}
	}